	LEFT  = 'left'  #pyautogui.press('left')
	RIGHT = 'right' #pyautogui.press('right')
	
	#Buttons
	A     = 'z'     #pyautogui.press('z')
	B     = 'x'     #pyautogui.press('x')
	
